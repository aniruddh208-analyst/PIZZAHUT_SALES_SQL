     = CAST(P.PIZZA_TYPE_ID AS VARCHAR(MAX))
JOIN ORDER_DETAILS OD
    ON CAST(OD.PIZZA_ID AS VARCHAR(MAX))
     = CAST(P.PIZZA_ID AS VARCHAR(MAX))
GROUP BY PT.CATEGORY
ORDER BY QUANTITY DESC;




